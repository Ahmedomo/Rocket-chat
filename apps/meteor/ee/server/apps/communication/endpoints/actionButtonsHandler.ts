import { AppsManager } from '@rocket.chat/core-services';

import { API } from '../../../../../app/api/server';

export const actionButtonsHandler = () =>
	[
		{
			authRequired: false,
		},
		{
			get(): any {
				const buttons = AppsManager.getAllActionButtons();

				return API.v1.success(buttons);
			},
		},
	] as const;
